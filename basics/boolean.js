/* BOOLEAN OPERATORS
===   equal
!==   not equal
<     less than
<=    less than or equal to
>     greater than
>=    greater than or equal to
*/

let temp = 31;
let isFreezing = temp === 31;
console.log(isFreezing);

let age = 21;
let isChild = age <= 7;
let isSenior = age >= 65;
console.log(isChild);
console.log(isSenior);